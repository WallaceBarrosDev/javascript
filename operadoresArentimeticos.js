"use strict"

const numero1 = 10
const numero2 = 2
let resultado = 0

resultado = numero1 + numero2
console.log(resultado) // soma

resultado = numero1 - numero2
console.log(resultado) // subtração

resultado = numero1 * numero2
console.log(resultado) // multiplicação

resultado = numero1 / numero2
console.log(resultado) // divisão

resultado = numero1 % numero2
console.log(resultado) // resto da divisão

resultado += numero1
console.log(resultado) // soma concstenada

resultado -= numero2
console.log(resultado) // subtração concatenada

console.log(resultado++)
console.log(resultado--)
